pub fn find_completions(prefix: &str) -> Vec<&'static str> {
    let stripped = prefix.strip_prefix('\\').unwrap_or(prefix);
    LATEX_COMMANDS
        .iter()
        .filter(|cmd| {
            cmd.strip_prefix('\\')
                .map(|c| c.starts_with(stripped))
                .unwrap_or(false)
                || cmd.starts_with(prefix)
        })
        .copied()
        .collect()
}
